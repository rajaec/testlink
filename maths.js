// Using let
let a = 10;
let b = 5;
let sum = a + b;
let difference = a - b;
let product = a * b;
let quotient = a / b;

console.log("Using let:");
console.log("Sum:", sum);
console.log("Difference:", difference);
console.log("Product:", product);
console.log("Quotient:", quotient);

// Using var
var x = 20;
var y = 4;
var sumVar = x + y;
var differenceVar = x - y;
var productVar = x * y;
var quotientVar = x / y;

console.log("\nUsing var:");
console.log("Sum:", sumVar);
console.log("Difference:", differenceVar);
console.log("Product:", productVar);
console.log("Quotient:", quotientVar);

// Using const (constant values)
const pi = 3.14;
const radius = 7;
const area = pi * radius * radius;

console.log("\nUsing const:");
console.log("Area of circle:", area);
